using System;

class Program
{
    static void Main()
    {
        int i = 0;  // Initialize the variable i to 0

        // Start a while loop that runs as long as i is less than 5
        whole (i < 5)
        {
            // Print the current value of i
            Console.WriteLine("The current value of i is: " + i);

            i++;  // Increment i by 1
        }
    }
}
